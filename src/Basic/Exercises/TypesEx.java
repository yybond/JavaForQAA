package Basic.Exercises;

//Объявить переменные всех вам известных типов и вывести их значения на экран.

public class TypesEx {
    public static void main(String[] args) {
        byte a = 127;
        short b = 23500;
        int c = 2147483555;
        double d = 13.0;
        char e = 65535;
        boolean f = false;

        System.out.println("\n" + a + "\n" + b + "\n" + c + "\n" + d + "\n" + e + "\n" + f );
    }
}
